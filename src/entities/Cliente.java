package entities;

	public class Cliente {
	
		private String nome;
		private String telefone;
		private String endereco;
		private int cpf;
		
		
	public Cliente(String nome, String telefone, String endereco, int cpf) {
		
		this.nome = nome;
		this.telefone = telefone;
		this.endereco = endereco;
		this.cpf = cpf;
		
	}	
		
}